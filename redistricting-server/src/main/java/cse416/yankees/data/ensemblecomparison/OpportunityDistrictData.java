package cse416.yankees.data.ensemblecomparison;

public class OpportunityDistrictData {
    private int RBPlans;
    private int VRAPlans;

    public OpportunityDistrictData(int RBPlans, int VRAPlans) {
        this.RBPlans = RBPlans;
        this.VRAPlans = VRAPlans;
    }

    public int getRBPlans() { return RBPlans; }
    public int getVRAPlans() { return VRAPlans; }
}
